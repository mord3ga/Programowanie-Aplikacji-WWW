<section class="center">
    <p>Literatura jest niezwykle różnorodna. Od mrocznych kryminałów, przez epickie fantasy, aż po literaturę faktu. Każdy gatunek oferuje inne emocje i doświadczenia. Oto kilka najpopularniejszych gatunków literackich:</p>
</section>
<div class="row">
    <section class="left">
        <article>
            <h2>1. Fantastyka</h2>
            <img src="images/fantasy.jpg" alt="książka fantasy">
            <p>Gatunek literacki używający magii i innych nadprzyrodzonych form jako głównego elementu fabuły, motywu, czy też scenerii. Często akcja dzieje się w fikcyjnych światach, zamieszkałych przez mityczne stworzenia.</p>
        </article>
        <br>
        <article>
            <h2>3. Kryminał</h2>
            <img src="images/crime.jpg" alt="kryminał">
            <p>Gatunek, którego fabuła koncentruje się wokół zbrodni, śledztwa i ujawnienia sprawcy. Trzyma w napięciu i zmusza czytelnika do logicznego myślenia wraz z detektywem.</p>
        </article>
        <br>
        <article>
            <h2>5. Powieść historyczna</h2>
            <img src="images/history_book.jpg" alt="powieść historyczna">
            <p>Utwór, którego akcja rozgrywa się w przeszłości. Autor stara się wiernie odwzorować realia danej epoki, często przeplatając losy postaci fikcyjnych z postaciami historycznymi.</p>
        </article>
        <br>
        <article>
            <h2>7. Romans</h2>
            <img src="images/romance.jpg" alt="romans">
            <p>Głównym wątkiem jest tutaj relacja uczuciowa między dwojgiem ludzi. Skupia się na emocjach, namiętności i dążeniu do szczęśliwego zakończenia, choć nie zawsze jest ono gwarantowane.</p>
        </article>
        <br>
    </section>
    <section class="right">
        <article>
            <h2>2. Science fiction</h2>
            <img src="images/scifi.jpg" alt="książka sci-fi">
            <p>Fantastyka naukowa, która zajmuje się wpływem nauki i techniki na jednostkę i społeczeństwo. Często wybiega w przyszłość, opisując podróże kosmiczne, roboty i obce cywilizacje.</p>
        </article>
        <br>
        <article>
            <h2>4. Horror</h2>
            <img src="images/horror.jpg" alt="horror">
            <p>Celem tego gatunku jest wywołanie u czytelnika lęku, strachu i niepokoju. Często wykorzystuje motywy zjawisk paranormalnych, potworów lub psychopatycznych morderców.</p>
        </article>
        <br>
        <article>
            <h2>6. Literatura faktu</h2>
            <img src="images/nonfiction.jpg" alt="reportaż">
            <p>Gatunek obejmujący reportaże, biografie, dzienniki i wspomnienia. Opisuje prawdziwe wydarzenia i postacie, starając się przedstawić rzeczywistość w sposób rzetelny i obiektywny.</p>
        </article>
        <br>
        <article>
            <h2>8. Poezja</h2>
            <img src="images/poetry.jpg" alt="tomik poezji">
            <p>Sztuka słowa, w której forma i rytm są równie ważne co treść. Poezja pozwala wyrazić najgłębsze uczucia i myśli w sposób skondensowany i metaforyczny.</p>
        </article>
        <br>
    </section>
</div>

